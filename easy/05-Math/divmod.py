# Enter your code here. Read input from STDIN. Print output to STDOUT
# Get two integer inputs from the user
a = int(input())
b = int(input())

# Print the integer division of a by b (i.e. a // b)
print(a // b)

# Print the remainder of a divided by b (i.e. a % b)
print(a % b)

# Print the result of divmod(a, b), which returns a tuple of (a // b, a % b)
print(divmod(a, b))


